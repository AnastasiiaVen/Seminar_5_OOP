package com.StreamAPI;

public class Worker {
    private int id;
    String name;
    String surname;
    int age;
    int salary;

    public Worker(int id, String name, String surname, int age, int salary) {
        this.id = id;
        this.name = name;
        this.surname = surname;
        this.age = age;
        this.salary = salary;
    }
}
